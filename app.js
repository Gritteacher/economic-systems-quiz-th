function getCategory(id = state.activeCategoryId) {
  return categories.find((category) => category.id === id);
}

function shuffle(values) {
  const items = [...values];
  for (let index = items.length - 1; index > 0; index -= 1) {
    const randomIndex = Math.floor(Math.random() * (index + 1));
    [items[index], items[randomIndex]] = [items[randomIndex], items[index]];
  }
  return items;
}

function resetCategoryCycle(categoryId) {
  const category = getCategory(categoryId);
  state.queues.set(
    categoryId,
    shuffle(category.questions.map((_, index) => index))
  );
  state.seen.set(categoryId, new Set());
}

function ensureCategoryCycle(categoryId) {
  const queue = state.queues.get(categoryId);
  if (!queue || queue.length === 0) {
    resetCategoryCycle(categoryId);
  }
}

function renderCategories() {
  categoryList.innerHTML = "";
  for (const category of categories) {
    const button = document.createElement("button");
    button.className = "category-button";
    button.type = "button";
    button.style.setProperty("--accent", category.accent);
    button.setAttribute("aria-pressed", String(category.id === state.activeCategoryId));
    button.dataset.categoryId = category.id;
    button.innerHTML = `
      <span class="category-name">${category.name}</span>
      <span class="category-size">${category.questions.length} ข้อ</span>
    `;
    button.addEventListener("click", () => selectCategory(category.id));
    categoryList.append(button);
  }
}

function renderQuestion() {
  const category = getCategory();
  const seen = state.seen.get(category.id) ?? new Set();

  document.documentElement.style.setProperty("--active-accent", category.accent);
  activeCategory.textContent = category.name;
  questionCount.textContent = `${seen.size}/${category.questions.length}`;
  questionText.textContent = state.currentQuestion.q;
  answerText.textContent = state.currentQuestion.a;
  answerPanel.hidden = true;
  revealButton.disabled = false;
  revealButton.focus({ preventScroll: true });
}

function drawQuestion() {
  const category = getCategory();
  ensureCategoryCycle(category.id);

  const queue = state.queues.get(category.id);
  const questionIndex = queue.pop();
  const seen = state.seen.get(category.id);
  seen.add(questionIndex);
  state.currentQuestion = category.questions[questionIndex];

  renderQuestion();
}

function selectCategory(categoryId) {
  state.activeCategoryId = categoryId;
  renderCategories();
  drawQuestion();
}

function revealAnswer() {
  answerPanel.hidden = false;
  revealButton.disabled = true;
}

function resetActiveCategory() {
  resetCategoryCycle(state.activeCategoryId);
  drawQuestion();
}

function validateQuestionCounts() {
  for (const category of categories) {
    if (category.questions.length !== 30) {
      throw new Error(`${category.name} must contain exactly 30 questions.`);
    }
  }
}

validateQuestionCounts();
renderCategories();
drawQuestion();

revealButton.addEventListener("click", revealAnswer);
nextButton.addEventListener("click", drawQuestion);
resetButton.addEventListener("click", resetActiveCategory);
